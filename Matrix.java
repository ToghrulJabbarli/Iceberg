/**
 * This is class is base class for matrix based objects.
 * @author Murad Shahsuvarov & Toghrul Jabbarli
 */
package Icefield;

public class Matrix {
	
	protected int height;
	protected int width;
	/**
	 * 
	 * @param height 
	 * @param width
	*/
	
	public Matrix(int height, int width)
	{
		this.height = height;
		this.width = width;
	}

}
